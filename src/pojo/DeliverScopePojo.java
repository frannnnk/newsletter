package pojo;

/**
 * 
 * @author 胡圣朗
 * 
 */
public class DeliverScopePojo {
	private String id;
	private String description;
	private String x_startcoordinate;
	private String y_startcoordinate;
	private String x_endcoordinate;
	private String y_endcoordinate;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getX_startcoordinate() {
		return x_startcoordinate;
	}

	public void setX_startcoordinate(String x_startcoordinate) {
		this.x_startcoordinate = x_startcoordinate;
	}

	public String getY_startcoordinate() {
		return y_startcoordinate;
	}

	public void setY_startcoordinate(String y_startcoordinate) {
		this.y_startcoordinate = y_startcoordinate;
	}

	public String getX_endcoordinate() {
		return x_endcoordinate;
	}

	public void setX_endcoordinate(String x_endcoordinate) {
		this.x_endcoordinate = x_endcoordinate;
	}

	public String getY_endcoordinate() {
		return y_endcoordinate;
	}

	public void setY_endcoordinate(String y_endcoordinate) {
		this.y_endcoordinate = y_endcoordinate;
	}

}
